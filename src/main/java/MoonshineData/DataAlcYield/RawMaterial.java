package MoonshineData.DataAlcYield;

public class RawMaterial {
    private double sugarMass;
    private double wheatMass;
    private double cornMass;
    private double potatoMass;

    public double getSugarMass() {
        return sugarMass;
    }

    public void setSugarMass(double sugarMass) {
        this.sugarMass = sugarMass;
    }

    public double getWheatMass() {
        return wheatMass;
    }

    public void setWheatMass(double wheatMass) {
        this.wheatMass = wheatMass;
    }

    public double getCornMass() {
        return cornMass;
    }

    public void setCornMass(double cornMass) {
        this.cornMass = cornMass;
    }

    public double getPotatoMass() {
        return potatoMass;
    }

    public void setPotatoMass(double potatoMass) {
        this.potatoMass = potatoMass;
    }
}
